package String_Tasks;

//Write a program to convert all the characters in a string to Lowercase

public class LowerCase16 {

	public static void main(String[] args) {

		String str = "I am java developer";

		String lower = str.toLowerCase();

		System.out.println("Original String is : " + str);
		System.out.println("Lowercase String is : " + lower);
	}

}

//Output:

//Original String is : I am java developer
//Lowercase String is : i am java developer
