package String_Tasks;

//Write a program to convert all the characters in a string to Uppercase

public class UpperCase17 {

	public static void main(String[] args) {

		String str = "Good Morning";

		String upper = str.toUpperCase();

		System.out.println("Original string is : " + str);
		System.out.println("Uppercase string is : " + upper);
	}

}

//Output:

//Original string is : Good Morning
//Uppercase string is : GOOD MORNING